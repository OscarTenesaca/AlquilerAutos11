package modelo;

public class Camion {
int idC;
String placa;
String tipo;
String marca;
int ano;
public Camion(){
	
}
public final int getIdC() {
	return idC;
}
public final void setIdC(int idC) {
	this.idC = idC;
}
public final String getPlaca() {
	return placa;
}
public final void setPlaca(String placa) {
	this.placa = placa;
}
public final String getTipo() {
	return tipo;
}
public final void setTipo(String tipo) {
	this.tipo = tipo;
}
public final String getMarca() {
	return marca;
}
public final void setMarca(String marca) {
	this.marca = marca;
}
public final int getAno() {
	return ano;
}
public final void setAno(int ano) {
	this.ano = ano;
}

}
